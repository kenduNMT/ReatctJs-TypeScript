// Khai báo 3 biến với kiểu dữ liệu number
let num1: number = 10;
let num2: number = 5;
let result: number;

// Thực hiện phép tính cộng và gán kết quả cho biến thứ 3
result = num1 + num2;
console.log(`Kết quả của ${num1} + ${num2} = ${result}`);

// Thực hiện phép tính trừ và gán kết quả cho biến thứ 3
result = num1 - num2;
console.log(`Kết quả của ${num1} - ${num2} = ${result}`);

// Thực hiện phép tính nhân và gán kết quả cho biến thứ 3
result = num1 * num2;
console.log(`Kết quả của ${num1} * ${num2} = ${result}`);

// Thực hiện phép tính chia và gán kết quả cho biến thứ 3
result = num1 / num2;
console.log(`Kết quả của ${num1} / ${num2} = ${result}`);
